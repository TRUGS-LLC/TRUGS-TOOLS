def render_aaa_trug(trug: Any) -> str:
    """Render AAA.md from an AAA TRUG dict/path/json-string."""
    data = _load_trug(trug)
    nmap = _node_map(data)
    root = _find_root(data)
    root_props = root.get("properties", {})

    phases = _node_children(data, root["id"])
    phases.sort(
        key=lambda n: (
            PHASE_ORDER.index(str(n.get("properties", {}).get("name", "")).upper())
            if str(n.get("properties", {}).get("name", "")).upper() in PHASE_ORDER
            else len(PHASE_ORDER),
            n.get("properties", {}).get("name", n.get("id", "")),
        )
    )

    lines: List[str] = [
        GENERATED_HEADER,
        "",
        f"# {root_props.get('name', 'AAA')}",
        "",
        f"**Issue Number:** {root_props.get('issue_number', '')}",
        f"**Folder:** {root_props.get('folder', '')}",
        f"**Status:** {root_props.get('status', '')}",
        f"**Created:** {root_props.get('created', '')}",
        "",
        "---",
        "",
    ]

    for phase in phases:
        if phase.get("type") != "PHASE":
            continue

        phase_props = phase.get("properties", {})
        phase_name = phase_props.get("name", phase.get("id", "PHASE"))
        phase_status = phase_props.get("status", "")
        phase_gate = phase_props.get("quality_gate", "")

        lines.append(f"## {phase_name}")
        lines.append("")
        lines.append(f"**Phase Status:** {phase_status}")
        lines.append(f"**Quality Gate:** {phase_gate}")
        lines.append("")

        if str(phase_status).upper() == "NOT_APPLICABLE":
            lines.append("*Not applicable for this project.*")
            lines.append("")
            continue

        children = _node_children(data, phase.get("id"))
        tasks = [n for n in children if n.get("type") == "TASK"]
        risks = [n for n in children if n.get("type") == "RISK"]
        dependencies = [n for n in children if n.get("type") == "DEPENDENCY"]
        sources = [n for n in children if n.get("type") == "RESEARCH_SOURCE"]
        adrs = [n for n in children if n.get("type") == "ADR"]
        gates = [n for n in children if n.get("type") == "QUALITY_GATE"]
        sub_issues = [n for n in children if n.get("type") == "SUB_ISSUE"]

        for task in tasks:
            lines.append(_task_line(task))
            for audit in _paired_audits(data, task.get("id", ""), nmap):
                audit_props = audit.get("properties", {})
                checked = "x" if _is_checked(audit_props.get("status")) else " "
                lines.append("  **AUDIT:**")
                lines.append(f"  - [{checked}] Gate 1 — Tests: {audit_props.get('gate_1_tests', '')}")
                lines.append(f"  - [{checked}] Gate 2 — Coverage: {audit_props.get('gate_2_coverage', '')}")
                lines.append(f"  - [{checked}] Gate 3 — Logic: {audit_props.get('gate_3_logic', '')}")
                lines.append(f"  - [{checked}] Gate 4 — Integration: {audit_props.get('gate_4_integration', '')}")
            lines.append("")

        risk_rows = [
            (
                n.get("properties", {}).get("name", n.get("id", "")),
                n.get("properties", {}).get("severity", ""),
                n.get("properties", {}).get("mitigation", ""),
                n.get("properties", {}).get("source", ""),
            )
            for n in risks
        ]
        _append_table(lines, ["Risk", "Severity", "Mitigation", "Source"], risk_rows)

        dependency_rows = [
            (
                n.get("properties", {}).get("name", n.get("id", "")),
                n.get("properties", {}).get("type", ""),
                n.get("properties", {}).get("version", ""),
                n.get("properties", {}).get("status", ""),
            )
            for n in dependencies
        ]
        _append_table(lines, ["Dependency", "Type", "Version", "Status"], dependency_rows)

        source_rows = [
            (
                n.get("properties", {}).get("name", n.get("id", "")),
                n.get("properties", {}).get("url", ""),
                n.get("properties", {}).get("accessed", ""),
                n.get("properties", {}).get("relevance", ""),
            )
            for n in sources
        ]
        _append_table(lines, ["Source", "URL", "Accessed", "Relevance"], source_rows)

        adr_rows = [
            (
                n.get("properties", {}).get("adr_id", ""),
                n.get("properties", {}).get("decision", ""),
                n.get("properties", {}).get("rationale", ""),
                n.get("properties", {}).get("status", ""),
            )
            for n in adrs
        ]
        _append_table(lines, ["ADR", "Decision", "Rationale", "Status"], adr_rows)

        for gate in gates:
            gate_props = gate.get("properties", {})
            checked = "x" if _is_checked(gate_props.get("status")) else " "
            lines.append(f"- [{checked}] {gate_props.get('name', gate.get('id', ''))}: {gate_props.get('condition', '')}")
        if gates:
            lines.append("")

        sub_rows = [
            (
                n.get("properties", {}).get("issue_number", ""),
                n.get("properties", {}).get("name", n.get("id", "")),
                n.get("properties", {}).get("type", ""),
                n.get("properties", {}).get("status", ""),
            )
            for n in sub_issues
        ]
        _append_table(lines, ["#", "Title", "Type", "Status"], sub_rows)

    return "\n".join(lines).rstrip() + "\n"
