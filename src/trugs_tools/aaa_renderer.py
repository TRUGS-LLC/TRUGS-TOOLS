"""AAA TRUG renderer - compiles aaa_v1 TRUG JSON into AAA.md markdown."""

import json
from pathlib import Path
from typing import Any, Dict, List, Tuple


GENERATED_HEADER = "<!-- GENERATED BY TRUGS — DO NOT EDIT -->"
PHASE_ORDER = [
    "VISION",
    "FEASIBILITY",
    "SPECIFICATIONS",
    "ARCHITECTURE",
    "CODING",
    "TESTING",
    "DEPLOYMENT",
]


def _load_trug(trug: Any) -> Dict[str, Any]:
    """Accept dict, path, or string; return parsed TRUG dict."""
    if isinstance(trug, dict):
        return trug
    if isinstance(trug, Path):
        return json.loads(trug.read_text(encoding="utf-8"))
    if isinstance(trug, str):
        stripped = trug.strip()
        if stripped.startswith("{"):
            return json.loads(stripped)
        return json.loads(Path(stripped).read_text(encoding="utf-8"))
    raise TypeError(f"Expected dict, str, or Path; got {type(trug).__name__}")


def _node_map(trug: Dict[str, Any]) -> Dict[str, Dict[str, Any]]:
    return {node.get("id"): node for node in trug.get("nodes", []) if node.get("id")}


def _node_children(trug: Dict[str, Any], parent_id: str) -> List[Dict[str, Any]]:
    children = [n for n in trug.get("nodes", []) if n.get("parent_id") == parent_id]
    children.sort(key=lambda n: (n.get("type", ""), n.get("properties", {}).get("name", n.get("id", ""))))
    return children


def _find_root(data: Dict[str, Any]) -> Dict[str, Any]:
    for node in data.get("nodes", []):
        if node.get("type") == "AAA":
            return node
    for node in data.get("nodes", []):
        if node.get("parent_id") is None:
            return node
    raise ValueError("No AAA root node found in TRUG (expected node type 'AAA' or parent_id=None)")


def _is_checked(status: Any) -> bool:
    value = str(status or "").upper()
    return value in {"COMPLETE", "PASS", "DONE", "✅"}


def _task_line(task: Dict[str, Any]) -> str:
    props = task.get("properties", {})
    checked = "x" if _is_checked(props.get("status")) else " "
    name = props.get("name", task.get("id", "unnamed task"))
    op = props.get("operation")
    if op:
        return f"- [{checked}] [{str(op).upper()}] {name}"
    return f"- [{checked}] {name}"


def _paired_audits(data: Dict[str, Any], task_id: str, nmap: Dict[str, Dict[str, Any]]) -> List[Dict[str, Any]]:
    audits: List[Dict[str, Any]] = []
    for edge in data.get("edges", []):
        if edge.get("relation") == "audits" and edge.get("to_id") == task_id:
            node = nmap.get(edge.get("from_id"))
            if node and node.get("type") == "AUDIT":
                audits.append(node)
    audits.sort(key=lambda n: n.get("properties", {}).get("name", n.get("id", "")))
    return audits


def _append_table(lines: List[str], headers: List[str], rows: List[Tuple[Any, ...]]) -> None:
    """Append a markdown table when rows are present."""
    if not rows:
        return
    lines.append(f"| {' | '.join(headers)} |")
    lines.append(f"| {' | '.join(['---'] * len(headers))} |")
    for row in rows:
        lines.append(f"| {' | '.join(str(v) for v in row)} |")
    lines.append("")


