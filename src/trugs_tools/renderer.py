def render_all(
        trug: Any,
        output_dir: Optional[str] = None,
        render_date: Optional[str] = None,
        repo_root: Optional[Union[str, Path]] = None) -> Dict[str, str]:
    """Render all three markdown files from a TRUG.

    Args:
        trug: TRUG dict, file path, or JSON string
        output_dir: Directory to write files. If None, returns content only.
        render_date: Fixed date string for deterministic output. Defaults to today.
        repo_root: Optional repository root used to load PROSE zone files.

    Returns:
        Dict mapping filename → rendered content
    """
    results = {
        "AAA.md": render_aaa(trug, render_date=render_date),
        "README.md": render_readme(trug, render_date=render_date),
        "ARCHITECTURE.md": render_architecture(trug, render_date=render_date, repo_root=repo_root),
    }

    if output_dir:
        out = Path(output_dir)
        out.mkdir(parents=True, exist_ok=True)
        for filename, content in results.items():
            (out / filename).write_text(content, encoding="utf-8")

    return results
