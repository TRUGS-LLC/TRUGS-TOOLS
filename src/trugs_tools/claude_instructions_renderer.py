"""claude_instructions_renderer — renders .github/agent_instructions.trug.json
into CLAUDE.md for Claude Code.

Output is deterministic: same TRUG input → byte-identical output.
"""

import json
from pathlib import Path
from typing import Any, Dict, List, Optional


GENERATED_HEADER = "<!-- This file is auto-generated by trugs-claude-render — do not edit directly. -->"
_SOURCE_COMMENT = "<!-- SOURCE OF TRUTH: .github/agent_instructions.trug.json -->"
_TITLE = "# CLAUDE.md"
_SUBTITLE = "This file provides guidance to Claude Code (claude.ai/code) when working with code in this repository."


# ─── Loader ───────────────────────────────────────────────────────────


def _load_trug(trug: Any) -> Dict[str, Any]:
    """Accept dict, path, or string; return parsed TRUG dict."""
    if isinstance(trug, dict):
        return trug
    if isinstance(trug, (str, Path)):
        with open(Path(trug), "r", encoding="utf-8") as f:
            return json.load(f)
    raise TypeError(f"Expected dict, str, or Path; got {type(trug)}")


# ─── TRL block helper ─────────────────────────────────────────────────


def _render_trl_block(instructions: List[str]) -> str:
    """Render a list of TRL instructions as a <trl> block."""
    if not instructions:
        return ""
    lines = ["", "<trl>"]
    for inst in instructions:
        lines.append(inst)
    lines.append("</trl>")
    return "\n".join(lines)


