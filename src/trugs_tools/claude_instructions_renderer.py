def _render_references(nodes: List[Dict]) -> str:
    """Render reference nodes as a compact list."""
    lines: List[str] = ["## Key References"]
    lines.append("")

    for n in nodes:
        p = n.get("properties", {})
        label = n.get("label", n.get("id", "Reference"))
        path = p.get("path", "")
        key_files = p.get("key_files", {})
        if key_files:
            first_file = next(iter(key_files))
            first_desc = key_files[first_file]
            lines.append(f"- **{label}:** `{path}` — start with `{first_file}` ({first_desc.lower().rstrip('.')})")
        else:
            lines.append(f"- **{label}:** `{path}`")

    return "\n".join(lines)


