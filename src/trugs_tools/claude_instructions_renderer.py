def _render_protected(constraint_nodes: List[Dict], archive_node: Optional[Dict]) -> str:
    """Render protected paths and deterministic files."""
    lines: List[str] = ["## Protected Paths — Never Modify"]
    lines.append("")

    all_trl: List[str] = []
    for n in constraint_nodes:
        p = n.get("properties", {})
        paths = p.get("paths", [])
        for path in paths:
            lines.append(f"- `{path}`")

        files = p.get("files", [])
        if files:
            gen = p.get("generator", "")
            for f in files:
                suffix = f" — auto-generated nightly by `{gen}`" if gen else ""
                lines.append(f"- `{f}` files{suffix}")

        all_trl.extend(p.get("trl_instruction", []))

    if archive_node:
        p = archive_node.get("properties", {})
        prefix = p.get("prefix", "zzz_")
        llm_rule = p.get("llm_rule", "")
        lines.append(f"- `{prefix}*` files/folders — Archived; {llm_rule.rstrip('.')}")
        all_trl.extend(p.get("trl_instruction", []))

    if all_trl:
        lines.append(_render_trl_block(all_trl))

    return "\n".join(lines)


