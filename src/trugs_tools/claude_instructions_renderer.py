# AGENT claude SHALL DEFINE FUNCTION render_claude_instructions.
def render_claude_instructions(
    trug: Any,
    render_date: Optional[str] = None,
) -> str:
    """Render agent_instructions.trug.json to CLAUDE.md content.

    Args:
        trug: TRUG dict, file path, or JSON string.
        render_date: Unused — accepted for API consistency with other renderers.

    Returns:
        Full markdown content for CLAUDE.md as a string.
    """
    data = _load_trug(trug)
    nodes = data.get("nodes", [])
    nmap: Dict[str, Dict] = {n["id"]: n for n in nodes}

    parts: List[str] = []

    # ── File header ──────────────────────────────────────────────────
    parts.append(_render_header())

    # ── 1. What This Repository Is ───────────────────────────────────
    if "tutorial_what_is_a_trug" in nmap:
        parts.append(_render_what_is_trugs(nmap["tutorial_what_is_a_trug"]))

    # ── 2. Protected paths ───────────────────────────────────────────
    constraint_nodes = [n for n in nodes if n.get("type") == "CONSTRAINT"]
    archive_node = nmap.get("convention_archive")
    if constraint_nodes or archive_node:
        parts.append(_render_protected(constraint_nodes, archive_node))

    # ── 3. Setup & Commands ──────────────────────────────────────────
    if "commands" in nmap:
        parts.append(_render_commands(nmap["commands"]))

    # ── 4. Development Workflow ──────────────────────────────────────
    if "rule_branching" in nmap:
        parts.append(_render_branching(nmap["rule_branching"]))

    # ── 4a. HITM ──────────────────────────────────────────────────────
    if "rule_hitm" in nmap:
        parts.append(_render_hitm(nmap["rule_hitm"]))

    # ── 4a2. Maintenance ─────────────────────────────────────────────
    if "workflow_maintenance" in nmap:
        parts.append(_render_maintenance(nmap["workflow_maintenance"]))

    # ── 4b. Four-File Pattern ────────────────────────────────────────
    if "standard_four_file_pattern" in nmap:
        parts.append(_render_four_file_pattern(nmap["standard_four_file_pattern"]))

    # ── 4b. Document Naming ──────────────────────────────────────────
    if "convention_naming" in nmap:
        parts.append(_render_naming(nmap["convention_naming"]))

    # ── 4c. folder.trug.json Rules ───────────────────────────────────
    if "rule_trug_build" in nmap:
        parts.append(_render_trug_build_rules(nmap["rule_trug_build"]))

    # ── 4d. Execution Rules ──────────────────────────────────────────
    if "rule_execution" in nmap:
        parts.append(_render_execution(nmap["rule_execution"]))

    # ── 4e. Motivation permanence ────────────────────────────────────
    if "rule_motivation_permanent" in nmap:
        parts.append(_render_motivation_permanent(nmap["rule_motivation_permanent"]))

    # ── 5. Claude Code Integration ─────────────────────────────────
    if "claude_code_integration" in nmap:
        parts.append(_render_claude_code_integration(nmap["claude_code_integration"]))

    # ── 5a. Read EPIC Before Work ────────────────────────────────────
    if "rule_epic_first" in nmap:
        parts.append(_render_epic_first(nmap["rule_epic_first"]))

    # ── 5b. TRL Vocabulary ───────────────────────────────────────────
    if "rule_trug_vocabulary" in nmap:
        parts.append(_render_trug_vocabulary(nmap["rule_trug_vocabulary"]))

    # ── 6. Architecture Notes ────────────────────────────────────────
    if "component_naming" in nmap:
        parts.append(_render_component_naming(nmap["component_naming"]))

    # ── 7. References ────────────────────────────────────────────────
    ref_nodes = [n for n in nodes if n.get("type") == "REFERENCE"]
    if ref_nodes:
        parts.append(_render_references(ref_nodes))

    return "\n\n".join(parts) + "\n"
